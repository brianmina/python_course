# Strings
name = "Eric"
print(f"Hello {name}!")

new_name = "Brian"
print(f"Hello {new_name.lower()}!")
print(f"Hello {new_name.upper()}!")
print(f"Hello {new_name.title()}!")

quote = "a person who never made a mistake never tried anything new."
author = "albert einstein"

print(f"{author.title()} once said, {quote.capitalize()}")


person_name = "\ttom\n"
print(person_name)
print(person_name.lstrip())
print(person_name.rstrip())
print(person_name.strip())


